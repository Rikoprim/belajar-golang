package main

import "fmt"

func main() {
	var name string

	name = "Riko Primada"
	fmt.Println(name)

	name = "Riko Primada Mania"
	fmt.Println(name)

	country := "Indonesia" //variable tanpa tipe
	fmt.Println(country)

	// multi variable

	var (
		firstname = "Riko"
		lastname = "Primadona"
	)

	fmt.Println(firstname)
	fmt.Println(lastname)
}
